<h3>3.1.3. Edição de dados de funcionários</h3>
<p>Ao acessar os dados de um funcionário é possível fazer a edição desses dados.</p>
<p>
	Na aba <strong>"Visão Geral"</strong> é possível editar as informações pessoais do funcionário.
	<p>
		Existem algumas seções nessa aba, são elas: Informações Pessoais, Endereço, Documentação e Outros. 
		Para fazer a edição de algum dado da página, basta verificar em qual seção ele se encontra e clicar no botão "Editar" da seção. Em seguida será posível alterar os dados, depois de alterados basta clicar no botão "Salvar".
	</p>
</p>
<p>Na aba <strong>"Benefícios"</strong> é possível adicionar e editar benefícios.</p>
<p>
	Para adicionar um benefício basta clicar no botão "Adicionar" no fim da página. Ao clicar nesse botão é aberta uma caixa para adicionar o benefício.
	<p>
		Para adicionar um benefício é necessário selecionar o benefício e o seu status e preencher a data de início, data de fim e o valor do benefício. Caso o benefício desejado não se encontre na listagem basta clicar no símbolo de "mais" ao seu lado e na nova caixa que será aberta escrever o nome do benefício e clicar em OK para salvá-lo.
	</p>
</p>
<p>
	Na seção de benefícios é possível visualizar a lista de benefícios do funcionário. Nessa lista é exibido o nome do benefício, o status, a data de início, a data de fim e valor. <br>
	Para editar um benefício basta clicar no botão verde ao seu lado, ao clicar o usuário é redirecionado para uma página onde é possível editar os dados desejados. Para editá-los basta clicar no botão "Editar" e após editar clicar no botão "salvar".
	Para excluir um benefício basta clicar no botão vermelho ao lado dos dados do benefício, o benefício será excluído automaticamente. 
</p>
<p>Na aba <strong>"EPI"</strong> é possível adicionar e editar EPI's.</p>
<p>
	Para adicionar um EPI basta clicar no botão "EPI" no fim da página. Ao clicar nesse botão é aberta uma caixa para adicionar o EPI.
	Para adicionar um EPI é necessário selecionar o EPI e o seu status e preencher a data. Caso o EPI desejado não se encontre na listagem basta clicar no símbolo de "mais" ao seu lado e na nova caixa que será aberta escrever o nome do EPI e clicar em OK para salvá-lo.
</p>
<p>
Na seção de EPI é possível visualizar a lista de EPI's do funcionário. Nessa lista é exibido o nome do epi, o status e a data. Para editar um EPI basta clicar no botão verde ao seu lado, ao clicar o usuário é redirecionado para uma página onde é possível ediar os dados desejados. Para editá-los basta clicar no botão "Editar" e após editar clicar no botão "Salvar".
Para excluir um EPI basta clicar no botão vermelho ao lado dos dados do EPI, o EPI será excluído automaticamente. 
<p>
<p>Na aba <strong>"Carga horária"</strong> é possível visualizar as informações da carga horária do funcionário, como escala, dias trabalhados, dias de folga, etc.</p>
<p>Na aba <strong>"Editar carga"</strong> é possível alterar ou cadastrar a carga horária do funcionário. Para alterar/cadastrar a carga horária basta selecionar a escala, o tipo, preencher os horários de entrada e saída e selecionar os dias folga e os dias trabalhados.</p>
